                    Main.dust[dust2].noGravity = true;
                    Main.dust[dust2].velocity *= 0f;
                    Main.dust[dust2].velocity *= 0f;
                    Main.dust[dust].scale = 2.9f;
					                    Main.dust[dust2].scale = 1.3f;
        }

